// Función para agregar IDs automáticamente
const agregarIds = (catalogo) => {
  const resultado = {};
  
  Object.entries(catalogo).forEach(([categoria, productos]) => {
    resultado[categoria] = productos.map((producto, index) => ({
      ...producto,
      id: `${categoria.toLowerCase().replace(/\s+/g, '-')}-${index}`
    }));
  });
  
  return resultado;
};

